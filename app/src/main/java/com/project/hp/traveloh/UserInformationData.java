public class UserInformationData {

    public UserInformationData() {
    }

    public String name;
    public String gender;
    public String email;
    public String dob;
    public String number;


    public UserInformationData(String name, String gender, String email, String dob, String number) {
        this.name = name;
        this.gender = gender;
        this.email = email;
        this.dob = dob;
        this.number = number;
    }


}




